  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    let that = this
    const db = wx.cloud.database()
    const user_id = getApp().globalData.user_id
    db.collection('Score').where({
      user_id: user_id
    }).orderBy('time', 'desc').field({
      score: true,
      time: true,
      activity_name: true
    }).get({
      success: res => {
        console.log(res.data)
        that.setData({
          logs: res.data
        })
      },
      fail: res => {
        console.log(res)
      }
    })
  },

